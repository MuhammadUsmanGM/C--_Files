#include <iostream>
using namespace std;
int main(){
    int x = 55, y = 10;
    cout<<x+y<<endl;
    cout<<x-y<<endl;
    cout<<x*y<<endl;
    cout<<x/y<<endl;
    float a = 55, b = 10;
    cout<<a+b<<endl;
    cout<<a-b<<endl;
    cout<<a*b<<endl;
    cout<<a/b<<endl;
    x++;//postincrement
    ++x;//preincrement
    cout<<x;
}